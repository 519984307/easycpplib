std::string uri_encode(const char *data, int len, const char tbl[]=full_encode_tbl)
{
    //from: https://github.com/eidheim/Simple-Web-Server/issues/11
    std::string out;
    const unsigned char *start = (unsigned char*)data;
    const unsigned char *end   = start + len;

    out.reserve(3*len + 1);
    while (start != end) {
        unsigned char c = *start++;
        if (tbl[c]) {
            out += c;
        } else {
            out += '%';
            out += DEC2HEX[c>>4];
            out += DEC2HEX[c&0xF];
        }
    }
    return out;
}

inline std::string uri_encode(const std::string& data) {
    return uri_encode(data.data(), data.size());
}

std::string uri_decode(const char *data, int len) {
    std::string out;
    char c, a, b;
    const unsigned char* start = (unsigned char*)data;
    const unsigned char* end   = start + len;

    out.reserve(len + 1);
    while (start != end) {
        c = *start++;
        if (c=='%') {
            if (start==end || (a=HEX2DEC[*start++])<0 ||
                start==end || (b=HEX2DEC[*start++])<0) {
                out.clear();
                break;
            }
            c=(a<<4)|b;
        }
        out += c;
    }
    return out;
}

inline std::string uri_decode(const std::string& data) {
    return uri_decode(data.data(), data.size());
}

